package model;

/**
 *
 * @author VulcanoMachine 2.0
 */
public abstract class ModelClass implements Cloneable{

    public Object clone() throws CloneNotSupportedException {
      Object clone = null;
      try {
         clone = super.clone();
      } catch (CloneNotSupportedException e) {
         e.printStackTrace();
      }
      return clone;
   }
}
